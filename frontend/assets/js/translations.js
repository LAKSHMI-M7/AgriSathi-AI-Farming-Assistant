function setLanguage(lang) {
    localStorage.setItem('agri_lang', lang);
    applyTranslations();
}

function getLanguage() {
    return localStorage.getItem('agri_lang') || 'en';
}

function applyTranslations() {
    const lang = getLanguage();
    const texts = translations[lang] || translations['en'];

    // Auto-translate elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (texts[key]) {
            if (element.tagName === 'INPUT') {
                element.placeholder = texts[key];
            } else {
                element.innerHTML = texts[key];
            }
        }
    });

    // Update active state of language buttons if they exist
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.classList.add('text-green-700', 'font-black');
            btn.classList.remove('text-gray-400');
        } else {
            btn.classList.remove('text-green-700', 'font-black');
            btn.classList.add('text-gray-400');
        }
    });
}

// Initial call when DOM is ready
document.addEventListener('DOMContentLoaded', applyTranslations);
